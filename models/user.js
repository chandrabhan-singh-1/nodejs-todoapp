import mongoose from "mongoose";

const apiSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    unique: true,
    required: true,
  },
  password: {
    type: String,
    select: false,
    required: true,
  },
  createdAt: {
    type: Date,
    default: new Date(),
  },
});

export const User = new mongoose.model("User", apiSchema);
